#!/usr/bin/env node
// One-off: extracts driver Name + Transporter ID from a Netradyne export
// and emits a SQL insert for public.drivers.
//
// Usage:
//   node scripts/seed-drivers-from-csv.mjs <path-to-csv> > supabase/seed-drivers.sql
//
// The proper bulk-import UI (with column mapping, dedup against ADP, etc.)
// ships in build order step 7. This is just to bootstrap step 2's list page
// with real data.

import fs from "node:fs";
import path from "node:path";
import Papa from "papaparse";

const csvPath = process.argv[2];
if (!csvPath) {
  console.error("usage: seed-drivers-from-csv.mjs <path-to-csv>");
  process.exit(1);
}

const raw = fs.readFileSync(csvPath, "utf8");

// The Netradyne export has ~9 lines of metadata before the real header.
// Find the header row by looking for the line that starts with "Driver Name,".
const lines = raw.split(/\r?\n/);
const headerIdx = lines.findIndex((l) => /^Driver Name\s*,/.test(l));
if (headerIdx === -1) {
  console.error("Could not locate 'Driver Name' header row in CSV.");
  process.exit(2);
}

const dataCsv = lines.slice(headerIdx).join("\n");
const parsed = Papa.parse(dataCsv, { header: true, skipEmptyLines: true });

const seen = new Set();
const rows = [];
for (const row of parsed.data) {
  const name = (row["Driver Name"] || "").trim();
  const tid = (row["Driver ID"] || "").trim();
  if (!name || !tid) continue;
  if (seen.has(tid)) continue;
  seen.add(tid);
  rows.push({ name, tid });
}

const sqlEscape = (s) => `'${String(s).replace(/'/g, "''")}'`;
const fileBase = path.basename(csvPath);

const sql = [
  `-- Seed: ${rows.length} drivers extracted from ${fileBase}.`,
  `-- Generated by scripts/seed-drivers-from-csv.mjs.`,
  `-- Replace once ADP roster import is wired up (build order step 7).`,
  ``,
  `insert into public.drivers (transporter_id, full_name, status, approved_vehicle_types)`,
  `values`,
  rows
    .map(
      ({ tid, name }) =>
        `  (${sqlEscape(tid)}, ${sqlEscape(name)}, 'active', '{}')`,
    )
    .join(",\n"),
  `on conflict (transporter_id) do nothing;`,
  ``,
].join("\n");

process.stdout.write(sql);
